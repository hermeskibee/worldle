def daily_word(date: datetime.date | None = None) -> str:
    """Deterministic word-of-the-day: same word for everyone on the same date."""
    date = date or datetime.date.today()
    # hash() is randomized per-process; use a stable digest so the same
    # date always maps to the same word across runs and machines.
    digest = hashlib.sha256(date.isoformat().encode()).hexdigest()
    index = int(digest, 16) % len(ANSWERS)
    return ANSWERS[index]


def random_word(exclude: str | None = None) -> str:
    """Pick a random answer, optionally avoiding a specific word (e.g. today's)."""
    pool = ANSWERS if exclude is None else tuple(w for w in ANSWERS if w != exclude)
    return random.choice(pool)


